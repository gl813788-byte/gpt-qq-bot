export function getLogDetailLabel(key, locale = "zh") {
  const value = String(key || "");
  if (!String(locale || "").toLowerCase().startsWith("zh")) return value;
  return detailLabelsZh[value] || value;
}

export function formatLogDetailValue(value, key = "", locale = "zh") {
  if (value == null || typeof value !== "string") return value;
  if (!String(locale || "").toLowerCase().startsWith("zh")) return value;
  const normalizedKey = String(key || "");
  const translated = detailValuesZh[normalizedKey]?.[value];
  if (translated) return translated;
  if (["reason", "decisionReason", "triggerReason", "error", "modelError"].includes(normalizedKey)) {
    return formatLogDetailText(value, locale);
  }
  if (normalizedKey === "source" && value.toLowerCase() === "onebot") return "OneBot";
  return value;
}

export function localizeLogDetails(value, locale = "zh", key = "", depth = 0) {
  if (!String(locale || "").toLowerCase().startsWith("zh")) return value;
  if (depth > 7 || value == null || typeof value === "number" || typeof value === "boolean") return value;
  if (typeof value === "string") return formatLogDetailValue(value, key, locale);
  if (Array.isArray(value)) {
    return value.map((item) => localizeLogDetails(item, locale, key, depth + 1));
  }
  if (typeof value === "object") {
    const output = {};
    for (const [itemKey, item] of Object.entries(value)) {
      output[getLogDetailLabel(itemKey, locale)] = localizeLogDetails(item, locale, itemKey, depth + 1);
    }
    return output;
  }
  return String(value);
}

export function compactLogDisplayText(value, maxLength = 720) {
  const text = singleLine(value);
  const limit = Math.max(40, Number(maxLength) || 720);
  return text.length > limit ? `${text.slice(0, limit - 1)}…` : text;
}

function translateCommonMessage(value) {
  const unable = value.match(/^Unable to (load|save|prepare|process|inspect|update|cleanup|validate) (.+)$/i);
  if (!unable) return value;
  const verbs = {
    load: "加载",
    save: "保存",
    prepare: "准备",
    process: "处理",
    inspect: "检查",
    update: "更新",
    cleanup: "清理",
    validate: "验证"
  };
  return `${verbs[unable[1].toLowerCase()] || unable[1]} ${unable[2]} 失败`;
}

function singleLine(value) {
  return String(value || "")
    .replace(/[\u0000-\u0008\u000b\u000c\u000e-\u001f\u007f]/g, "")
    .replace(/\s+/g, " ")
    .trim();
}
