export function formatLogMessage(message, locale = "zh") {
  const value = String(message || "");
  if (!String(locale || "").toLowerCase().startsWith("zh")) return value;
  return messageTranslationsZh[value] || translateCommonMessage(value);
}

export function formatLogDetailText(value, locale = "zh") {
  const text = singleLine(value);
  if (!String(locale || "").toLowerCase().startsWith("zh")) return text;
  if (detailTranslationsZh[text]) return detailTranslationsZh[text];
  return text
    .replace(/^model judge failed:\s*/i, "判定模型失败：")
    .replace(/OpenRouter judge did not return valid FINAL_JSON/gi, "OpenRouter 判定模型未返回有效结构化结果")
    .replace(/OpenRouter judge produced no new token for (\d+)ms/gi, "OpenRouter 判定模型连续 $1ms 未返回新 token")
    .replace(/attempt timed out after (\d+)ms/gi, "单次请求超过 $1ms")
    .replace(/search timed out/gi, "整次搜索超时")
    .replace(/returned HTTP (\d+)/gi, "返回 HTTP $1")
    .replace(/returned verification page/gi, "返回验证页")
    .replace(/Tavily API key is not configured/gi, "没有配置 Tavily API key")
    .replace(/all search providers returned no results/gi, "所有搜索服务都没有结果")
    .replace(/all search providers failed/gi, "所有搜索服务都失败")
    .replace(/no results/gi, "没有解析到结果")
    .replace(/Access blocked by Cloudflare/gi, "访问被 Cloudflare 拦截")
    .replace(/Blocked private or unresolved address for ([^\s]+)/gi, "域名 $1 解析到私网、保留地址或无法解析，已拦截")
    .replace(/connect ECONNREFUSED ([^\s]+)/gi, "连接 $1 被拒绝")
    .replace(/fetch failed/gi, "网络请求失败");
}

export function formatLogError(value, locale = "zh") {
  if (value == null || value === "") return "";
  if (typeof value !== "object") return formatLogDetailText(value, locale);
  const message = formatLogDetailText(value.message || value.error || "", locale);
  const code = value.code || value.errno || value.status || "";
  const endpoint = [value.address, value.port].filter((item) => item != null && item !== "").join(":");
  const cause = value.cause ? formatLogError(value.cause, locale) : "";
  const parts = [message, code && !message.includes(String(code)) ? String(code) : "", endpoint && !message.includes(endpoint) ? endpoint : "", cause]
    .filter(Boolean)
    .filter((item, index, list) => list.indexOf(item) === index);
  if (parts.length > 0) return parts.join("；");
  return formatLogDetailText(value.name || "Unknown error", locale);
}

export function getLogLevelLabel(level, locale = "zh") {
  const value = String(level || "info").toLowerCase();
  return String(locale || "").toLowerCase().startsWith("zh") ? (levelLabels[value] || value) : value.toUpperCase();
}

export function getLogCategoryLabel(category, locale = "zh") {
  const value = String(category || "system").toLowerCase();
  return String(locale || "").toLowerCase().startsWith("zh") ? (categoryLabels[value] || value) : value;
}

export function compactLogDisplayText(value, maxLength = 720) {
  const text = singleLine(value);
  const limit = Math.max(40, Number(maxLength) || 720);
  return text.length > limit ? `${text.slice(0, limit - 1)}…` : text;
}

function translateCommonMessage(value) {
  const unable = value.match(/^Unable to (load|save|prepare|process|inspect|update|cleanup|validate) (.+)$/i);
  if (!unable) return value;
  const verbs = {
    load: "加载",
    save: "保存",
    prepare: "准备",
    process: "处理",
    inspect: "检查",
    update: "更新",
    cleanup: "清理",
    validate: "验证"
  };
  return `${verbs[unable[1].toLowerCase()] || unable[1]} ${unable[2]} 失败`;
}

function singleLine(value) {
  return String(value || "")
    .replace(/[\u0000-\u0008\u000b\u000c\u000e-\u001f\u007f]/g, "")
    .replace(/\s+/g, " ")
    .trim();
}
