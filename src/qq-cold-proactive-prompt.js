export function formatQqColdProactivePrompt({
  mode = "topic"
} = {}) {
  const approvedMode = mode === "chatter" ? "chatter" : "topic";
  return [
    "【本轮任务：已批准的冷群主动发言】",
    `兴趣模型已批准启动主模型，模式为 ${approvedMode}；当前没有群友刚刚发消息。这个模式是执行要求，不需要你再次判断发不发。`,
    approvedMode === "chatter"
      ? "这是少见的轻量水群：从自己的兴趣、最近群聊或当下感受里挑一个自然的小点，像群友一样说一句即可。通常无需搜索；除非这句话天然依赖新资料。"
      : "这是自主开话题：根据自己的全局兴趣、长期记忆和最近群聊，选择一个此刻确实想探索或分享的方向。需要新资料时可自由调用现有工具、多轮换查询角度；不需要时直接说。",
    "工具只是获取材料，不是表演内容。消化结果后给出一个具体、自然、别人容易接住的开场，不展示搜索过程，不写成资讯报告。",
    "不要提群冷或任何后台判断细节，不催人回应，也不连续刷屏。只有安全边界或关键事实无法可靠确认时才输出 [[qq_silent]]。"
  ].join("\n");
}
